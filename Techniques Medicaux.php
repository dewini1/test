<!--more-->
<p>Ces nouvelles ont été diffusées sur la <a href="/fr/">Communauté PetalMD</a> au courant de l’hiver 2017. Pour ne rien manquer des innovations en médecine, n’hésitez pas à vous inscrire!</p>
<h2>1. Un casque qui lit les pensées</h2>
<p>Des scientifiques ont développé un moyen de communication pour les patients paralysés et incapables de parler en mesurant le niveau d'oxygène du sang dans le cerveau, ce qui permet de déchiffrer les pensées.&nbsp;</p>
<p>
</p><div class="hs-responsive-embed hs-responsive-embed-youtube"><iframe class="hs-responsive-embed-iframe" xml="lang" src="//www.youtube.com/watch?v=KngKWsVZg80" width="560" height="314" allowfullscreen="" data-service="youtube"></iframe></div>
<p></p>
<p>&nbsp;</p>
<h2>2. De la peau qui s'imprime</h2>
<p>Les laboratoires de l’Université de Wake Forest en Caroline du Nord espèrent accélérer le processus de guérison des patients gravement brûlés à l’aide d’une nouvelle technologie d'impression 3D.</p>
<p>
</p><div class="hs-responsive-embed hs-responsive-embed-youtube"><iframe class="hs-responsive-embed-iframe" xml="lang" src="//www.youtube.com/embed/q8DnF0tF7D0?rel=0" width="560" height="314" allowfullscreen="" data-service="youtube"></iframe></div>
<p></p>
<p>&nbsp;</p>
<h2>3. Un bras mécanique branché sur le cerveau</h2>
<p>Des chercheurs ont développé une prothèse de bras robotique qui agit selon la pensée du patient grâce à des capteurs qui détectent les signaux provenant des nerfs de la moelle épinière.</p>
<p>En savoir plus :&nbsp;<a href="http://www.nature.com/articles/s41551-016-0025">Man/machine interface based on the discharge timings of spinal motor neurons after targeted muscle reinnervation</a></p>
<p><img src="https://www.petalmd.com/hs-fs/hubfs/Web/blog/top1.png?width=625&amp;name=top1.png" alt="Un bras mécanique branché sur le cerveau" width="625" title="Un bras mécanique branché sur le cerveau" caption="false" data-constrained="true" style="width: 625px;" srcset="https://www.petalmd.com/hs-fs/hubfs/Web/blog/top1.png?width=313&amp;name=top1.png 313w, https://www.petalmd.com/hs-fs/hubfs/Web/blog/top1.png?width=625&amp;name=top1.png 625w, https://www.petalmd.com/hs-fs/hubfs/Web/blog/top1.png?width=938&amp;name=top1.png 938w, https://www.petalmd.com/hs-fs/hubfs/Web/blog/top1.png?width=1250&amp;name=top1.png 1250w, https://www.petalmd.com/hs-fs/hubfs/Web/blog/top1.png?width=1563&amp;name=top1.png 1563w, https://www.petalmd.com/hs-fs/hubfs/Web/blog/top1.png?width=1875&amp;name=top1.png 1875w" sizes="(max-width: 625px) 100vw, 625px"></p>

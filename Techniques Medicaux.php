<h2>4. Des électrochocs qui aident les victimes d'un AVC</h2>
<p>Un dispositif conçu pour l’auto-réhabilitation des survivants d’un AVC permettrait de renforcer les connexions entre le cerveau et la colonne vertébrale grâce à l’émission de petits chocs électriques.&nbsp;</p>
<p>En savoir plus :&nbsp;<a href="https://www.youtube.com/watch?v=M6vExchEAMc">Stroke device could allow self-rehabilitation</a></p>
